import java.util.*;
public class typecasting {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        float num=99.999f;
        int num2=(int)num;
        System.out.println(num2);
        char ch='a';
        char ch2='b';
        int number=ch;
        int number2=ch2;
        System.out.println(number);
        System.out.println(number2);
        sc.close();
    }
}
