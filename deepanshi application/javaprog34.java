public class javaprog34 {
    public static int ab(int a,int b){
        return a+b;
    }
    public static int abc(int a,int b,int c){
        return a+b+c;
    }
    public static void main(String args[]){
        System.out.println("Sum of two numbers is : "+ab(4,5));
        System.out.println("Sum of three numbers is : "+abc(5,2,2));
    }
}
